package sec3;

import java.util.Scanner;	//자바로 시작하지 않으면 사용자가 만든 패키지로 들어감

public class Example5 {

	public static void main(String[] args) {
		//키보드와 모니터를 통해 입출력이 가능한 태그
		
		Scanner sc = new Scanner(System.in);	//스캐너는 클래스, sc는 객체. sc를 입력 가능한 객체로 만들기 위해 시스템.in 입력
		System.out.println("이름 입력: ");
		String name = sc.nextLine();
		
		System.out.println("국어 점수 입력[0~100]: ");
		int kor = sc.nextInt();
		
		System.out.println("수학 점수 입력[0~100]: ");
		int mat = sc.nextInt();
		
		System.out.println("영어 점수 입력[0~100]: ");
		int eng = sc.nextInt();
		
		System.out.println("이름: "+name);
		System.out.println("국어: "+kor);
		System.out.println("수학: "+mat);
		System.out.println("영어: "+eng);
		System.out.println("총점: "+(kor+mat+eng));
		System.out.println("평균: "+((kor+mat+eng)/3.0f));
		
	}

}
